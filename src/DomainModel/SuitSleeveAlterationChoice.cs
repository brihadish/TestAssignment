namespace DomainModel
{
    /// <summary>
    /// Specifies the choice of sleeve alteration.
    /// </summary>
    public enum SuitSleeveAlterationChoice
    {
        /// <summary>
        /// Specifies that only the left sleeve is to be altered.
        /// </summary>
